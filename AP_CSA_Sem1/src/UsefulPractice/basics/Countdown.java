package UsefulPractice.basics;

public class Countdown {
    public static void main(String[] args){
        int x = 10;
        while(x>=1){
            System.out.println(x);
            x--;
        }
        System.out.println();
        for(int i = 10; i>=1; i--){
            System.out.println(i);
        }

    }
}
